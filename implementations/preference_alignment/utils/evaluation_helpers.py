"""Helpers for evaluating model outputs using rule-based metrics and LLM judges."""

import json
import re
from asyncio import Semaphore
from typing import Any

import torch
from openai import AsyncOpenAI
from transformers import AutoTokenizer


def extract_qa(conv_text: str) -> tuple[str | Any, str | Any, str | Any]:
    """
    Parse a comparison prompt to extract the core question and candidates.

    Parameters
    ----------
    conv_text : str
        The raw conversation text containing Question and Answer sections.

    Returns
    -------
    question : str
        The extracted question.
    a1 : str
        The content of Answer 1.
    a2 : str
        The content of Answer 2.
    """
    q = re.search(r"Question:(.*?)(?:Answer 1:|$)", conv_text, re.DOTALL)
    a1 = re.search(r"Answer 1:(.*?)(?:Answer 2:|$)", conv_text, re.DOTALL)
    a2 = re.search(r"Answer 2:(.*)", conv_text, re.DOTALL)
    return (
        q.group(1).strip() if q else "",
        a1.group(1).strip() if a1 else "",
        a2.group(1).strip() if a2 else "",
    )


def safe_json_loads(text: str) -> Any:
    """
    Load JSON content from a string, recovering from common formatting errors.

    Will attempt to find a JSON block `{...}` if a direct load fails.

    Parameters
    ----------
    text : str
        The input string.

    Returns
    -------
    Any
        The parsed Python object.

    Raises
    ------
    json.JSONDecodeError
        If no JSON-like structure is found or it remains unparseable.
    """
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        match = re.search(r"\{.*\}", text, re.DOTALL)
        if match:
            return json.loads(match.group(0))
        raise


def run_local_inference(
    model,
    tokenizer: AutoTokenizer,
    prompt: str,
    max_new_tokens: int,
    temperature: float,
) -> Any:
    """
    Execute generation on the local model for a specific evaluation task.

    Parameters
    ----------
    model : object
        The LLM implementation.
    tokenizer : AutoTokenizer
        The model's tokenizer.
    prompt : str
        The instruction for the model.
    max_new_tokens : int
        Limit for the length of output.
    temperature : float
        The randomness governing the generation.

    Returns
    -------
    str
        The decoded text generated by the model.
    """
    inputs = tokenizer(prompt, return_tensors="pt", truncation=True).to(model.device)

    with torch.inference_mode():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            temperature=temperature,
            pad_token_id=tokenizer.eos_token_id,
        )

    decoded = tokenizer.decode(outputs[0], skip_special_tokens=True)
    return decoded[len(prompt) :].strip()


async def judge_with_openai(
    client: AsyncOpenAI,
    semaphore: Semaphore,
    judge_model: str,
    q: str,
    a1: str,
    a2: str,
    base_out: str,
    dpo_out: str,
) -> Any:
    """
    Compare two model outputs using an external flagship LLM (OpenAI).

    Scores each output (base vs DPO) on quality dimensions using an
    asynchronous API call protected by a semaphore for rate control.

    Parameters
    ----------
    client : AsyncOpenAI
        The OpenAI API client.
    semaphore : Semaphore
        Asyncio semaphore to limit concurrent requests.
    judge_model : str
        Name of the OpenAI model to use as a judge.
    q : str
        The original question.
    a1 : str
        Prompt answer candidate 1.
    a2 : str
        Prompt answer candidate 2.
    base_out : str
        Evaluation text from the base model.
    dpo_out : str
        Evaluation text from the DPO-trained model.

    Returns
    -------
    dict
        The parsed scores and reasoning from the judge.
    """
    query = f"""
You are an impartial expert evaluator.

Evaluate the following two model judgments independently on a scale from 0 to 10
based on:
- coherence
- accuracy
- coverage
- overall quality

Question:
{q}

Answer 1:
{a1}

Answer 2:
{a2}

Model A (Base model) judgment:
{base_out}

Model B (DPO-trained model) judgment:
{dpo_out}

Respond ONLY in valid JSON:

{{
  "base_score": number,
  "dpo_score": number,
  "reason": "short explanation"
}}
"""

    async with semaphore:
        r = await client.chat.completions.create(
            model=judge_model,
            messages=[{"role": "user", "content": query}],
            temperature=0,
        )

    raw = r.choices[0].message.content.strip()
    return safe_json_loads(raw)
